package com.lzzyyy.xxk.user.pojo;

import java.sql.Time;
import java.sql.Timestamp;

/**
 * 
 * @作者 yaxiu
 * @时间 2015-9-1 工作记录，记录员工的工作项，由员工自己添加
 */
public class WorkRecord {

	private Integer wrId;
	private String wrName;
	private String wrRemark;
	private Timestamp wrStTime;// 工作开始时间
	private Timestamp wrEndTime;// 工作完成时间
	private Integer wordState;// 工作状态
	private User user;

	public Integer getWrId() {
		return wrId;
	}

	public void setWrId(Integer wrId) {
		this.wrId = wrId;
	}

	public String getWrName() {
		return wrName;
	}

	public void setWrName(String wrName) {
		this.wrName = wrName;
	}

	public String getWrRemark() {
		return wrRemark;
	}

	public void setWrRemark(String wrRemark) {
		this.wrRemark = wrRemark;
	}

	public Timestamp getWrStTime() {
		return wrStTime;
	}

	public void setWrStTime(Timestamp wrStTime) {
		this.wrStTime = wrStTime;
	}

	public Timestamp getWrEndTime() {
		return wrEndTime;
	}

	public void setWrEndTime(Timestamp wrEndTime) {
		this.wrEndTime = wrEndTime;
	}

	public Integer getWordState() {
		return wordState;
	}

	public void setWordState(Integer wordState) {
		this.wordState = wordState;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

}
